import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('loan_approval_large_dataset.csv')

data

data.describe(include='all')

data.isnull().sum()

data['Gender'] = data['Gender'].map({'Male':1, 'Female':0})
data['Married'] = data['Married'].map({'Yes':1, 'No':0})
data['Loan_Status'] = data['Loan_Status'].map({'Y':1, 'N':0})

data

X = data.drop('Loan_Status', axis=1)
y = data['Loan_Status']

X

y

from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.2, stratify=y
)

X_train.shape

X_test.shape

from sklearn.linear_model import LogisticRegression

model1 = LogisticRegression(class_weight='balanced')

model1.fit(X_train,y_train)

from sklearn.ensemble import RandomForestClassifier

model2 = RandomForestClassifier(class_weight='balanced')
model2.fit(X_train, y_train)

y_pred1 = model1.predict(X_test)
y_pred2 = model2.predict(X_test)

y_pred1

y_pred2

from sklearn.metrics import accuracy_score

print("Logistic: \n", accuracy_score(y_test, y_pred1))
print("Random Forest:", accuracy_score(y_test, y_pred2))

from sklearn.metrics import confusion_matrix, classification_report,ConfusionMatrixDisplay
print("Logistic: \n",confusion_matrix(y_test,y_pred1))
print("Random forest: \n",confusion_matrix(y_test, y_pred2))

cm = confusion_matrix(y_test,y_pred1)

cm

disp = ConfusionMatrixDisplay(confusion_matrix=cm,display_labels=model1.classes_)
disp.plot()
plt.grid(False)
plt.show()

def predict_loan(gender, married, income, loan_amount, credit):
    prediction = model1.predict([[gender, married, income, loan_amount, credit]])[0]

    if prediction == 1:
        return "Loan Approved ✅"
    else:
        return "Loan Rejected ❌"

